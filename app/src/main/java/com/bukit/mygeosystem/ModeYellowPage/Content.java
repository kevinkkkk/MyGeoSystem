public class Content {


    private Video Video;

    private Photo Photo;

    private Profile Profile;

    private DspAd DspAd;

    private Logo Logo;

    private Url Url;


    public Video getVideo() {
        return Video;
    }


    public void setVideo(Video Video) {
        this.Video = Video;
    }


    public Photo getPhoto() {
        return Photo;
    }


    public void setPhoto(Photo Photo) {
        this.Photo = Photo;
    }


    public Profile getProfile() {
        return Profile;
    }


    public void setProfile(Profile Profile) {
        this.Profile = Profile;
    }


    public DspAd getDspAd() {
        return DspAd;
    }


    public void setDspAd(DspAd DspAd) {
        this.DspAd = DspAd;
    }


    public Logo getLogo() {
        return Logo;
    }


    public void setLogo(Logo Logo) {
        this.Logo = Logo;
    }


    public Url getUrl() {
        return Url;
    }


    public void setUrl(Url Url) {
        this.Url = Url;
    }

}